DROP TABLE KINDSTORE;

CREATE SEQUENCE KINDNUMBER_SEQ

CREATE TABLE KINDSTORE
(
    KINDNUMBER          NUMBER            NOT NULL,
    KINDDO          VARCHAR2(4000)     NOT NULL,
    KINDCITY          VARCHAR2(4000)     NOT NULL,
    KINDTYPE    VARCHAR2(4000)     NOT NULL,
    KINDNAME       VARCHAR2(4000)     NOT NULL,
    KINDPHONE       VARCHAR2(4000)     NOT NULL,
    KINDADDR          VARCHAR2(4000)     NOT NULL,
    KINDDELIVERY        VARCHAR2(4000)     NOT NULL,
    KINDPARKING        VARCHAR2(4000)    NOT NULL,
    KINDMENU        VARCHAR2(4000)      NOT NULL,
    CONSTRAINT KINDSTORE_PK PRIMARY KEY(KINDNUMBER)
);

